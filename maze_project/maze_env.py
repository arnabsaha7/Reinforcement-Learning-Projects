import numpy as np
import matplotlib.pyplot as plt
from matplotlib.patches import Arrow

class Maze:
    def __init__(self, maze, start, goal):
        self.maze = maze
        self.start = start
        self.goal = goal
        self.maze_height = maze.shape[0]
        self.maze_width = maze.shape[1]

    def show_maze(self):
        plt.figure(figsize=(5, 5))
        plt.imshow(self.maze, cmap="binary", interpolation="nearest")
        plt.text(self.start[1], self.start[0], "S", ha='center', va='center', color='red', fontsize=20)
        plt.text(self.goal[1], self.goal[0], "G", ha='center', va='center', color='green', fontsize=20)
        plt.xticks([]), plt.yticks([])
        plt.show()

